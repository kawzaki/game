final_questions = []

# Generate 50 questions per category by repeating/padding with different values
for cat, data in categories.items():
    prefix = "".join([w[0] for w in cat.split()]).lower()
    if cat == "الجغرافيا": prefix = "g"
    elif cat == "التاريخ": prefix = "h"
    elif cat == "العلوم": prefix = "s"
    elif cat == "الدين": prefix = "r"
    elif cat == "الرياضة": prefix = "sp"
    elif cat == "شخصيات": prefix = "p"
    elif cat == "معلومات عامة": prefix = "gk"

    for i in range(50):
        val = [100, 200, 300, 400, 500][i % 5]
        q_data = data[i % len(data)]
        
        # Add some variety to ids
        q_id = f"{prefix}-{i+1}"
        
        final_questions.append({
            "id": q_id,
            "category": cat,
            "value": val,
            "question": q_data[0] if i < len(data) else f"{q_data[0]} (مستوى {i//len(data) + 1})",
            "answer": q_data[1],
            "options": q_data[2],
            "isAnswered": False
        })

# Add Luck Questions
